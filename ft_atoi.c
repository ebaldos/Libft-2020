#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		y;
	int		x;

	x = 0;
	i = 0;
	y = 1;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n' || str[i] == '\r'
			|| str[i] == '\t' || str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			y = -y;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		x = x * 10 + (str[i] - 48);
		i++;
	}
	x = y * x;
	return (x);
}
